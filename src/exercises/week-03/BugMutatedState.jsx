import { useState } from "react";
export default function BugMutatedState() {
  const [count, setCount] = useState(0);

   function handleAdd() {
    count++;
    setCount(count);
  }

  return (
    <div>
      <p>Bug 2 Count: {count}</p>
      <button onClick={handleAdd}>Add 1</button>
    </div>
  );
}

